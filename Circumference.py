#A program that finds the square inches and perimiter of a rectangle

length = int(input("Please enter a rectangle length: "))
width = int(input("Please enter a width for a rectangle: "))
square_inch = length*width
perimeter = 2*length+2*width
print("Length:", length)
print("Width:", width)
print("Square Inch:", square_inch)
print("Perimeter:", perimeter)
